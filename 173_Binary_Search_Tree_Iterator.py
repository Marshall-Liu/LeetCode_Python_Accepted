# Non-recursive inorder traversal

# Definition for a  binary tree node
# class TreeNode(object):
#     def __init__(self, x):
#         self.val = x
#         self.left = None
#         self.right = None

class BSTIterator(object):
    def __init__(self, root):
        """
        :type root: TreeNode
        """
        self.stack, p = [], root
        while p:
            self.stack.append(p)
            p = p.left

    def hasNext(self):
        """
        :rtype: bool
        """
        return self.stack

    def next(self):
        """
        :rtype: int
        """
        node = self.stack.pop()
        p = node.right
        while p:
            self.stack.append(p)
            p = p.left
        return node.val

